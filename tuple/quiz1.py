# 튜플을 생성하고 월요일, 화요일, 수요일을 넣으세요
week = ('월요일','화요일','수요일')
print(week)

# 튜플을 생성하고 서울, 부산,대구를 넣으세요
city = ('서울', '부산', '대구')
print(city)

# 튜플을 생성하고,
# True, 23.5 , "맑음"을 넣으세요
# 파이썬의 튜플은 숫자, 문자, bool을 섞어서 저장 가능
weather = (True, 23.5, "맑음")
print(weather)

# 튜플을 생성하고 30을 넣으세요
# 요소가 하나일때는 (,)콤마로 구분
t =(30,)
