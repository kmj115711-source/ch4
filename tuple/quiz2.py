# 튜플 생성
nums = (10,20,30,40,50,60)

# 인덱스 2번부터 4번까지 잘라서 출력하세요
# 결과 : (30,40,50)
print(nums[2:5])

# 처음부터 인덱스 3번 전까지 잘라서 출력하세요
# 결과 : (10,20,30)
print(nums[:3])

# 인덱스 2번부터 끝까지 잘라서 출력하세요
# 결과 : (30,40,50,60)
print(nums[2:])

# 새 튜플
letters = ('a','b','c','d','e','f','g')

# 인덱스 2번부터 5번까지 잘라서 출력하세요
# 결과 :('c','d','e','f')
print(letters[2:6])

# 처음부터 인덱스 2번 전까지 잘라서 출력하세요
# 결과 : ('a','b')
print(letters[:2])

# 인덱스 4번부터 끝까지 잘라서 출력하세요
# 결과 :('e','f','g')
print(letters[4:])
